def train_and_evaluate(config, train_loader, val_loader, test_loader):
    """
    Trains a specific model configuration and returns metrics and history.
    """
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    set_seed(42) # Reproducibility requirement [Sources]

    # Initialize Model
    model = TransformerClassifier(
        vocab_size=5, # PAD, A, B, C, D [Sources]
        d_model=config['d_model'],
        num_heads=config['num_heads'],
        d_ff=config['d_ff'],
        num_layers=config['num_layers'],
        use_pe=config['use_pe']
    ).to(device)

    param_count = sum(p.numel() for p in model.parameters())
    optimizer = torch.optim.Adam(model.parameters(), lr=0.001)
    criterion = nn.BCELoss()

    history = {'train_loss': [], 'val_acc': []}
    start_time = time.time()

    # Training Loop
    for epoch in range(config['epochs']):
        model.train()
        total_loss = 0
        for tokens, masks, labels in train_loader:
            tokens, masks, labels = tokens.to(device), masks.to(device), labels.to(device)
            optimizer.zero_grad()
            outputs = model(tokens, masks)
            loss = criterion(outputs, labels)
            loss.backward()
            optimizer.step()
            total_loss += loss.item()

        # Epoch Validation
        model.eval()
        correct, total = 0, 0
        with torch.no_grad():
            for tokens, masks, labels in val_loader:
                tokens, masks, labels = tokens.to(device), masks.to(device), labels.to(device)
                outputs = model(tokens, masks)
                acc = calculate_accuracy(outputs, labels)
                correct += acc.item() * labels.size(0)
                total += labels.size(0)

        val_acc = correct / total
        history['train_loss'].append(total_loss / len(train_loader))
        history['val_acc'].append(val_acc)

    training_time = time.time() - start_time

    # Final Test Accuracy
    model.eval()
    correct, total = 0, 0
    with torch.no_grad():
        for tokens, masks, labels in test_loader:
            tokens, masks, labels = tokens.to(device), masks.to(device), labels.to(device)
            outputs = model(tokens, masks)
            acc = calculate_accuracy(outputs, labels)
            correct += acc.item() * labels.size(0)
            total += labels.size(0)
    test_acc = correct / total

    return {
        "Variant": config['name'],
        "Val Acc": round(val_acc, 4),
        "Test Acc": round(test_acc, 4),
        "Time (s)": round(training_time, 2),
        "Params": param_count
    }, history

